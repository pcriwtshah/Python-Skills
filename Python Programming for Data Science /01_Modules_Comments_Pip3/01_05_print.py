#These next 4 lines will print in a different line.
print("Hello,")
print("How")
print("are")
print("you?")

#These next 4 lines will print in same line without space between words becasue of end=""
print("Hello,", end="")
print("How", end="")
print("are", end="")
print("you")


#These next 4 lines will print in the same line with spaces between words because of end=" "
print("Hello,", end=" ")
print("How", end=" ")
print("are", end=" ")
print("you?")

#This will print in 3 different lines becasue of \n
print("Hello,\n how are\n you?")
